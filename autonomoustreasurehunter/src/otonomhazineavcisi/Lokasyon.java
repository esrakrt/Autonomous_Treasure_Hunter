package otonomhazineavcisi;

public class Lokasyon {
    private int x;
    private int y;

    public Lokasyon(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getter metodları buraya eklenmiş olmalı
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    // Setter metodları buraya eklenmiş olmalı
    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}